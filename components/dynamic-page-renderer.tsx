'use client';

import React from 'react';
import { BevGeniePage, PageSection } from '@/lib/ai/page-specs';
import { COLORS } from '@/lib/constants/colors';
import { Download, Share2, ExternalLink } from 'lucide-react';

interface DynamicPageRendererProps {
  page: BevGeniePage;
  onDownload?: () => void;
  onShare?: () => void;
  onNavigationClick?: (action: string, context?: any) => void;
  compact?: boolean; // For displaying in chat vs full page
}

/**
 * Dynamic Page Renderer
 *
 * Converts BevGenie page specifications (JSON) into rendered React components.
 * Supports 6 page types and 8 section types with consistent styling.
 */
export function DynamicPageRenderer({
  page,
  onDownload,
  onShare,
  onNavigationClick,
  compact = false,
}: DynamicPageRendererProps) {
  return (
    <div className={`dynamic-page-renderer ${compact ? 'compact' : 'full'}`}>
      {/* Header with title and actions */}
      {!compact && (
        <div
          className="page-header border-b p-6 mb-6"
          style={{
            backgroundColor: COLORS.white,
            borderColor: COLORS.mediumGray,
          }}
        >
          <div className="flex items-start justify-between mb-3">
            <div>
              <h1
                className="text-3xl font-bold"
                style={{ color: COLORS.navy }}
              >
                {page.title}
              </h1>
              <p className="mt-2" style={{ color: COLORS.textGray }}>
                {page.description}
              </p>
            </div>
            {(onDownload || onShare) && (
              <div className="flex gap-2">
                {onDownload && (
                  <button
                    onClick={onDownload}
                    className="flex items-center gap-2 px-4 py-2 rounded-lg transition-colors"
                    style={{
                      backgroundColor: COLORS.white,
                      borderColor: COLORS.mediumGray,
                      color: COLORS.navy,
                      border: `1px solid ${COLORS.mediumGray}`,
                    }}
                    title="Download as PDF"
                  >
                    <Download className="w-4 h-4" />
                    <span className="text-sm font-medium hidden sm:inline">Download</span>
                  </button>
                )}
                {onShare && (
                  <button
                    onClick={onShare}
                    className="flex items-center gap-2 px-4 py-2 rounded-lg transition-colors"
                    style={{
                      backgroundColor: COLORS.white,
                      borderColor: COLORS.mediumGray,
                      color: COLORS.navy,
                      border: `1px solid ${COLORS.mediumGray}`,
                    }}
                    title="Share this page"
                  >
                    <Share2 className="w-4 h-4" />
                    <span className="text-sm font-medium hidden sm:inline">Share</span>
                  </button>
                )}
              </div>
            )}
          </div>
          <div className="flex gap-2 mt-4 flex-wrap">
            <span
              className="inline-block px-3 py-1 text-xs font-semibold rounded-full"
              style={{
                backgroundColor: `${COLORS.cyan}22`,
                color: COLORS.cyan,
              }}
            >
              {page.type.replace(/_/g, ' ')}
            </span>
            <span
              className="inline-block px-3 py-1 text-xs font-semibold rounded-full"
              style={{
                backgroundColor: `${COLORS.navy}22`,
                color: COLORS.navy,
              }}
            >
              BevGenie Solution
            </span>
          </div>
        </div>
      )}

      {/* Render sections */}
      <div className={`page-content space-y-8 ${compact ? 'max-w-2xl' : ''}`}>
        {page.sections.map((section, index) => (
          <SectionRenderer
            key={index}
            section={section}
            index={index}
            onNavigationClick={onNavigationClick}
          />
        ))}
      </div>

      {/* Footer in full view */}
      {!compact && (
        <div
          className="page-footer border-t p-6 mt-8 text-center"
          style={{
            backgroundColor: COLORS.lightGray,
            borderColor: COLORS.mediumGray,
          }}
        >
          <p className="text-sm" style={{ color: COLORS.textGray }}>
            Generated by{' '}
            <span className="font-semibold" style={{ color: COLORS.cyan }}>
              BevGenie AI
            </span>
            <br />
            Personalized for your beverage business needs
          </p>
        </div>
      )}
    </div>
  );
}

/**
 * Section Renderer
 * Routes to appropriate section component based on section type
 */
function SectionRenderer({
  section,
  index,
  onNavigationClick,
}: {
  section: PageSection;
  index: number;
  onNavigationClick?: (action: string, context?: any) => void;
}) {
  switch (section.type) {
    case 'hero':
      return <HeroSection section={section} onNavigationClick={onNavigationClick} />;
    case 'feature_grid':
      return <FeatureGridSection section={section} onNavigationClick={onNavigationClick} />;
    case 'testimonial':
      return <TestimonialSection section={section} />;
    case 'comparison_table':
      return <ComparisonTableSection section={section} />;
    case 'cta':
      return <CTASection section={section} onNavigationClick={onNavigationClick} />;
    case 'faq':
      return <FAQSection section={section} />;
    case 'metrics':
      return <MetricsSection section={section} />;
    case 'steps':
      return <StepsSection section={section} onNavigationClick={onNavigationClick} />;
    default:
      return (
        <div className="p-4 bg-red-50 border border-red-200 rounded-lg text-red-800">
          Unknown section type: {(section as any).type}
        </div>
      );
  }
}

/**
 * Hero Section Component
 * Large headline with optional CTA button - Professional B2B SaaS styling
 */
function HeroSection({
  section,
  onNavigationClick,
}: {
  section: any;
  onNavigationClick?: (action: string, context?: any) => void;
}) {
  const handleCtaClick = () => {
    if (onNavigationClick) {
      onNavigationClick(section.ctaButton?.action || 'hero_cta_click', {
        text: section.ctaButton?.text,
        context: section.headline,
      });
    }
  };

  const handleLearnMore = () => {
    if (onNavigationClick) {
      onNavigationClick('learn_more', {
        context: section.headline,
      });
    }
  };

  return (
    <div
      className="hero-section py-16 px-12 rounded-xl"
      style={{
        backgroundColor: `linear-gradient(135deg, ${COLORS.navy}15, ${COLORS.cyan}10)`,
        border: `1px solid ${COLORS.mediumGray}`,
      }}
    >
      <div className="max-w-3xl">
        <h2
          className="text-5xl md:text-6xl font-bold mb-6 leading-tight"
          style={{ color: COLORS.navy }}
        >
          {section.headline}
        </h2>
        {section.subheadline && (
          <p
            className="text-xl md:text-2xl mb-10 leading-relaxed font-medium"
            style={{ color: COLORS.darkGray }}
          >
            {section.subheadline}
          </p>
        )}
        {section.ctaButton && (
          <div className="flex flex-wrap gap-4 mt-8">
            <button
              onClick={handleCtaClick}
              className="px-8 py-4 font-bold rounded-lg transition-all hover:shadow-lg transform hover:scale-105 text-white"
              style={{ backgroundColor: COLORS.cyan }}
            >
              {section.ctaButton.text}
            </button>
            <button
              onClick={handleLearnMore}
              className="px-8 py-4 font-bold rounded-lg transition-all hover:shadow-lg border-2"
              style={{
                borderColor: COLORS.navy,
                color: COLORS.navy,
                backgroundColor: COLORS.white,
              }}
            >
              Learn More
            </button>
          </div>
        )}
      </div>
    </div>
  );
}

