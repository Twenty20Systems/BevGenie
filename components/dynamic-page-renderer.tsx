function ComparisonTableSection({ section }: { section: any }) {
  return (
    <div className="comparison-table-section">
      {section.title && (
        <h3
          className="text-2xl font-bold mb-6"
          style={{ color: COLORS.navy }}
        >
          {section.title}
        </h3>
      )}

      <div className="overflow-x-auto">
        <table className="w-full border-collapse">
          <thead>
            <tr style={{ backgroundColor: COLORS.navy, color: COLORS.white }}>
              {section.headers.map((header: string, idx: number) => (
                <th
                  key={idx}
                  className="px-6 py-4 text-left font-semibold border"
                  style={{ borderColor: COLORS.mediumGray }}
                >
                  {header}
                </th>
              ))}
            </tr>
          </thead>
          <tbody>
            {section.rows.map((row: any, rowIdx: number) => (
              <tr
                key={rowIdx}
                style={{
                  backgroundColor:
                    rowIdx % 2 === 0 ? COLORS.white : COLORS.lightGray,
                }}
              >
                <td
                  className="px-6 py-4 font-semibold border"
                  style={{
                    color: COLORS.navy,
                    borderColor: COLORS.mediumGray,
                  }}
                >
                  {row.feature}
                </td>
                {row.values.map((value: any, colIdx: number) => (
                  <td
                    key={colIdx}
                    className="px-6 py-4 border text-center"
                    style={{
                      color: COLORS.darkGray,
                      borderColor: COLORS.mediumGray,
                    }}
                  >
                    {typeof value === 'boolean' ? (
                      value ? (
                        <span style={{ color: COLORS.cyan, fontWeight: 'bold' }}>
                          ✓
                        </span>
                      ) : (
                        <span style={{ color: COLORS.copper, fontWeight: 'bold' }}>
                          ✗
                        </span>
                      )
                    ) : (
                      value
                    )}
                  </td>
                ))}
              </tr>
            ))}
          </tbody>
        </table>
      </div>
    </div>
  );
}

/**
 * CTA Section Component
 * Call-to-action buttons with various actions - Professional B2B SaaS styling
 */
function CTASection({
  section,
  onNavigationClick,
}: {
  section: any;
  onNavigationClick?: (action: string, context?: any) => void;
}) {
  const handleButtonClick = (button: any) => {
    if (onNavigationClick) {
      onNavigationClick(button.action || 'cta_click', {
        text: button.text,
        context: section.title,
        isPrimary: button.primary,
      });
    }
  };

  return (
    <div
      className="cta-section p-16 rounded-2xl shadow-2xl text-center text-white"
      style={{
        backgroundColor: `linear-gradient(135deg, ${COLORS.navy}, #003d7a)`,
      }}
    >
      <h3 className="text-4xl md:text-5xl font-bold mb-4">{section.title}</h3>
      {section.description && (
        <p className="text-lg mb-12 max-w-2xl mx-auto leading-relaxed" style={{ color: '#d1d5db' }}>
          {section.description}
        </p>
      )}

      <div className="flex flex-wrap gap-4 justify-center mt-10">
        {section.buttons.map((button: any, idx: number) => (
          <button
            key={idx}
            onClick={() => handleButtonClick(button)}
            className={`px-8 py-4 font-bold rounded-lg transition-all hover:shadow-xl transform hover:scale-105 ${
              button.primary ? 'text-navy' : ''
            }`}
            style={{
              backgroundColor: button.primary ? COLORS.cyan : 'transparent',
              border: button.primary ? 'none' : `2px solid ${COLORS.cyan}`,
              color: button.primary ? COLORS.navy : COLORS.cyan,
            }}
          >
            {button.text}
          </button>
        ))}
      </div>
    </div>
  );
}

/**
 * FAQ Section Component
 * Accordion-style frequently asked questions
 */
function FAQSection({ section }: { section: any }) {
  const [expandedIndex, setExpandedIndex] = React.useState<number | null>(null);

  return (
    <div className="faq-section">
      {section.title && (
        <h3
          className="text-2xl font-bold mb-6"
          style={{ color: COLORS.navy }}
        >
          {section.title}
        </h3>
      )}

      <div className="space-y-3">
        {section.items.map((item: any, idx: number) => (
          <div
            key={idx}
            className="rounded-lg overflow-hidden hover:shadow-md transition-shadow"
            style={{
              backgroundColor: COLORS.white,
              border: `1px solid ${COLORS.mediumGray}`,
            }}
          >
            <button
              onClick={() =>
                setExpandedIndex(expandedIndex === idx ? null : idx)
              }
              className="w-full px-6 py-4 text-left font-semibold flex items-center justify-between transition-colors"
              style={{
                backgroundColor: COLORS.lightGray,
                color: COLORS.navy,
              }}
            >
              {item.question}
              <span
                className={`transform transition-transform ${
                  expandedIndex === idx ? 'rotate-180' : ''
                }`}
              >
                ▼
              </span>
            </button>
            {expandedIndex === idx && (
              <div
                className="px-6 py-4 border-t"
                style={{
                  color: COLORS.darkGray,
                  backgroundColor: COLORS.white,
                  borderColor: COLORS.mediumGray,
                }}
              >
                {item.answer}
              </div>
            )}
          </div>
        ))}
      </div>
    </div>
  );
}

